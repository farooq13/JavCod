public class Arithmetic {
    public static void main(String[] args) {
        int x = 3;
        int y = 2;

        int add = x + y;

        int sub = x - y;

        int mul = x * y;

        System.out.println(x + " + " +  y + " = " + add);
        System.out.println(x + " - " +  y + " = " + sub);
        System.out.println(x + " * " +  y + " = " + mul);

        // 1:00:00
    }
}